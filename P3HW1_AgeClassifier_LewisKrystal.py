#Use the if/else structure to determine a person's age

#Get input from user


age = int(input("The age of the person is: "))

if age <= 1:
    print("He/She is an infant.")

elif age <= 13:
    print("He/She is a child.")

elif age <= 20:
    print ("He/She is a teenager.")

else:
    print("He/she is an adult.")
    
    
    

